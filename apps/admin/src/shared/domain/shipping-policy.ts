// 배송 정책 조회기 — **자리만** 만든다 (정책 문서의 정본은 상품 관리의 배송 정책이 갖는다)
//
// ─────────────────────────────────────────────────────────────────────────────
// [왜 이 파일이 있나 — 환불액에서 뺄 반품배송비가 두 곳에서 정해지고 있었다]
// 반품 환불액은 '결제액 − 반품배송비 − 회수 쿠폰분' 이다. 그 반품배송비의 기본값은 배송 정책
// (/products/shipping 의 returnFee)이 이미 갖고 있는데, 클레임 화면이 그 값을 모르면 운영자가
// 매번 손으로 적게 되고 — 정책은 3,000원인데 어떤 환불은 2,500원으로 나가는 일이 생긴다.
//
// [왜 클레임이 배송 정책을 직접 읽지 않나]
// `pages/orders → pages/products` 는 페이지 간 결합이다(축1 · blocker · 임계값 0건). 그래서 공통
// 층은 계약과 등록기만 갖고, 두 도메인을 모두 아는 `src/wiring.ts` 가 구현을 꽂는다.
//
// [왜 숫자 하나만 넘기나]
// 클레임에 필요한 것은 '반품배송비 기본값' 뿐이다. 권역 추가금·묶음배송·조건부 무료 같은 나머지는
// 배송 정책이 소유하는 축이고, 계약에 섞으면 클레임이 배송 규칙을 다시 해석하기 시작한다.
//
// [모르면 null — 0 이 아니다]
// 0 을 주면 화면은 '반품배송비 0원' 이라는 **완결된 사실**을 그리고 운영자는 그대로 환불한다.
// 조용히 덜 빼는 쪽으로 틀리는 기본값이라 특히 위험하다. 그래서 모를 때는 모른다고 말하고, 화면은
// 입력칸을 비운 채 '정책을 불러오지 못했다' 고 밝힌다(order-ref.ts 와 같은 규약).
// ─────────────────────────────────────────────────────────────────────────────

/** null = '모른다'(미배선이거나 정책을 아직 불러오지 못했다) */
type ReturnFeeLookup = () => number | null;

let lookup: ReturnFeeLookup | null = null;

/** 조회기를 꽂는다 — 여러 번 불러도 결과가 같다(멱등). 호출자는 `src/wiring.ts` 하나다 */
export function registerReturnFeeLookup(next: ReturnFeeLookup): void {
  lookup = next;
}

/** 테스트가 미배선 상태로 되돌린다 — '모른다' 경로를 실제로 밟아 보기 위한 것이다 */
export function resetReturnFeeLookup(): void {
  lookup = null;
}

/**
 * 배송 정책이 정한 반품배송비 — **모르면 null**.
 *
 * 음수·NaN 은 정책 문서의 자유 입력에서 나올 수 있는 값이라 여기서 걸러 null 로 만든다. 그것을
 * 그대로 흘리면 '환불액이 결제액보다 큰' 계산이 조용히 성립한다.
 */
export function policyReturnFee(): number | null {
  if (lookup === null) return null;
  const fee = lookup();
  if (fee === null || !Number.isFinite(fee) || fee < 0) return null;
  return fee;
}
