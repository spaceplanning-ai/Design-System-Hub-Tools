// 배포용 배럴 생성기 — src/ds/**/<Name>/<Name>.tsx 의 공개 export를 모아 src/lib.ts 로 재수출.
// 컴포넌트(PascalCase function/const) + 타입(export type/interface)을 이름 중복 제거하며 수집.
// 사용: node scripts/gen-lib-barrel.mjs
import { readFileSync, writeFileSync, existsSync, readdirSync, statSync } from 'node:fs'
import { resolve, dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const dsDir = join(root, 'src', 'ds')

// 컴포넌트 디렉터리 수집: src/ds/<Name> 및 src/ds/kr/<Name>
const dirs = []
for (const name of readdirSync(dsDir)) {
  const p = join(dsDir, name)
  if (!statSync(p).isDirectory()) continue
  if (name === 'kr') {
    for (const kn of readdirSync(p)) {
      const kp = join(p, kn)
      if (statSync(kp).isDirectory()) dirs.push({ dir: kp, base: kn, rel: `./ds/kr/${kn}/${kn}` })
    }
  } else {
    dirs.push({ dir: p, base: name, rel: `./ds/${name}/${name}` })
  }
}

const RESERVED = new Set(['default'])
const seen = new Map() // exportName -> rel (첫 등장)
const valueLines = []
const typeLines = []
const skipped = []

for (const { dir, base, rel } of dirs.sort((a, b) => a.base.localeCompare(b.base))) {
  const file = join(dir, `${base}.tsx`)
  if (!existsSync(file)) continue // logos/ 같은 비컴포넌트 폴더
  const src = readFileSync(file, 'utf8')
  const values = new Set()
  const types = new Set()
  // export function Foo / export const Foo = / export const Foo: — PascalCase만
  for (const m of src.matchAll(/export\s+(?:async\s+)?function\s+([A-Z]\w*)/g)) values.add(m[1])
  for (const m of src.matchAll(/export\s+const\s+([A-Z]\w*)\s*[:=]/g)) values.add(m[1])
  // export type Foo / export interface Foo
  for (const m of src.matchAll(/export\s+type\s+([A-Z]\w*)/g)) types.add(m[1])
  for (const m of src.matchAll(/export\s+interface\s+([A-Z]\w*)/g)) types.add(m[1])

  const vs = [...values].filter((n) => !RESERVED.has(n) && !dedupe(n, rel))
  const ts = [...types].filter((n) => !RESERVED.has(n) && !dedupe(n, rel))
  if (vs.length) valueLines.push(`export { ${vs.join(', ')} } from '${rel}'`)
  if (ts.length) typeLines.push(`export type { ${ts.join(', ')} } from '${rel}'`)
  if (!vs.length && !ts.length) skipped.push(base)
}

function dedupe(name, rel) {
  if (seen.has(name)) {
    if (seen.get(name) !== rel) console.warn(`중복 export '${name}' — ${rel} 건너뜀(이미 ${seen.get(name)})`)
    return true
  }
  seen.set(name, rel)
  return false
}

const out = `// AUTO-GENERATED by scripts/gen-lib-barrel.mjs — DO NOT EDIT.
// @figam-dev-variable-tools/design-kit 공개 진입점. 모든 DS 컴포넌트 + prop 타입을 재수출.
${valueLines.join('\n')}

${typeLines.join('\n')}
`
writeFileSync(join(root, 'src', 'lib.ts'), out)
console.log(`gen-lib-barrel OK — 컴포넌트 export ${valueLines.length}파일, 타입 ${typeLines.length}파일 → src/lib.ts`)
if (skipped.length) console.log(`export 미발견(건너뜀): ${skipped.join(', ')}`)
